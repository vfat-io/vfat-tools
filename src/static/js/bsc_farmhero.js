  async function getBscHEROPoolInfo(app, chefContract, poolIndex, pendingRewardsFunction) {
    const poolInfo = await chefContract.poolInfo(poolIndex);
    if (poolInfo.allocPoint == 0 || poolInfo.poolType!=0 || poolInfo.accAUTOPerShare == 0) {
      return {
        address: poolInfo.want,
        allocPoints: poolInfo.allocPoint ?? 1,
        poolToken: null,
        erc20: poolInfo.poolType==0,
        userStaked : 0,
        pendingRewardTokens : 0,
        stakedToken : null,
        userLPStaked : 0,
        lastRewardTime : poolInfo.lastRewardTime
      };
    }
    const poolToken = await getBscToken(app, poolInfo.want, poolInfo.strat); //staked is 0
    const strat = new ethers.Contract(poolInfo.strat, STRAT_ABI, app.provider);
    poolToken.staked = await strat.wantLockedTotal() / 1e18;
    const totalShares = await strat.sharesTotal();
    const userInfo = await chefContract.userInfo(poolIndex, app.YOUR_ADDRESS);
    const pendingRewardTokens = await chefContract.callStatic[pendingRewardsFunction](poolIndex, app.YOUR_ADDRESS);
    const userStaked = userInfo.shares / totalShares * poolToken.staked;
    return {
        address: poolInfo.want,
        allocPoints: poolInfo.allocPoint ?? 1,
        poolToken,
        erc20: poolInfo.poolType==0,
        userStaked,
        pendingRewardTokens : pendingRewardTokens / 10 ** 18,
        stakedToken : null,
        userLPStaked : null,
        lastRewardTime : poolInfo.lastRewardTime
    };
  }
