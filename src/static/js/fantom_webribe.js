async function main() {
  const App = await init_ethers()

  _print(`Initialized ${App.YOUR_ADDRESS}\n`)
  _print('Reading smart contracts...\n')

  const VBRB_CHEF_ADDR = '0x3b646B690d75628C49489A2A453E168A343d0Bb0'

  const rewardTokenTicker = 'vBRB'
  const VBRB_CHEF = new ethers.Contract(VBRB_CHEF_ADDR, VBRB_CHEF_ABI, App.provider)

  let rewardsPerWeek = 0
  const startBlock = await VBRB_CHEF.startBlock()
  const currentBlock = await App.provider.getBlockNumber()

  if (currentBlock < startBlock) {
    _print(
      `Rewards start at block <a href="https://ftmscan.com/block/countdown/${startBlock}" target="_blank">${startBlock}</a>\n`
    )
  } else {
    rewardsPerWeek = ((await VBRB_CHEF.rewardsPerBlock()) / 1e18) * 604800
  }

  const tokens = {}
  const prices = await getFantomPrices()

  await loadFantomChefContract(
    App,
    tokens,
    prices,
    VBRB_CHEF,
    VBRB_CHEF_ADDR,
    VBRB_CHEF_ABI,
    rewardTokenTicker,
    'rewardsToken',
    null,
    rewardsPerWeek,
    'pendingRewards'
  )

  hideLoading()
}
