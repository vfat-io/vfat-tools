async function main() {
  const App = await init_ethers()

  _print(`Initialized ${App.YOUR_ADDRESS}\n`)
  _print('Reading smart contracts...\n')

  const POPIT_CHEF_ADDR = '0xa356097644cB4e0bCF939aCE69A719849521Cd8B'
  const rewardTokenTicker = 'POPIT'
  const POPIT_CHEF = new ethers.Contract(POPIT_CHEF_ADDR, POPIT_CHEF_ABI, App.provider)

  const tokens = {}
  const prices = await getBscPrices()

  let rewardsPerWeek = 0
  const startBlock = await POPIT_CHEF.startBlock()
  const currentBlock = await App.provider.getBlockNumber()

  if (startBlock > currentBlock) {
    _print(`Rewards have not started yet, they will start at block ${startBlock}\n`)
  } else {
    rewardsPerWeek = (((await POPIT_CHEF.popitPerBlock()) / 1e18) * 604800) / 3
    await loadBscChefContract(
      App,
      tokens,
      prices,
      POPIT_CHEF,
      POPIT_CHEF_ADDR,
      POPIT_CHEF_ABI,
      rewardTokenTicker,
      'popit',
      null,
      rewardsPerWeek,
      'pendingPopit'
    )
  }

  hideLoading()
}
