async function main() {
  const App = await init_ethers();

  _print(`Initialized ${App.YOUR_ADDRESS}\n`);
  _print("Reading smart contracts...\n");

  const CHEF_ADDR = "0x1c8079c981E7774589c5C8B52686151046EfAFeF";
  const rewardTokenTicker = "NERD";
  const CHEF = new ethers.Contract(CHEF_ADDR, CHEF_ABI, App.provider);

  const startTime = await CHEF.startTimestamp();
  const currentTime = Date.now();
  let rewardsPerWeek = 0;

  if (currentTime > startTime) {
    rewardsPerWeek = await CHEF.joePerSec() / 1e18 * 604800;
  } else {
    _print("Rewards has not started yet")
    _print("")
  }


  const tokens = {};
  const prices = await getAvaxPrices();

  await loadAvaxChefContract(App, tokens, prices, CHEF, CHEF_ADDR, CHEF_ABI, rewardTokenTicker,
    "joe", null, rewardsPerWeek, "pendingTokens");

  hideLoading();
}

